#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*dest;
	size_t			i;

	dest = s;
	i = 0;
	if (!s)
		return ;
	while (i++ < n)
	{
		*(dest++) = 0;
	}
}
