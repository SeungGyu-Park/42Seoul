#include <unistd.h>

void	ft_is_negative(int n)
{
	char	ch;

	if (n < 0)
	{
		ch = 'N';
		write(1, &ch, 1);
	}
	else
	{
		ch = 'P';
		write(1, &ch, 1);
	}
}
